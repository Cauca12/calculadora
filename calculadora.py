# Calculadora básica

def sumar(a, b):
    return a + b

def restar(a, b):
    return a - b

def multiplicar(a, b):
    return a * b    

def dividir(a, b):
    return a / b

print("La suma es: ",sumar(10, 5))
print("La resta es: ", restar(10, 5))
print("La division es: ",dividir(10,2))
print("La multiplicacion es: " , multiplicar(10, 5))